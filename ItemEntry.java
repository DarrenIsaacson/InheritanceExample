
public class ItemEntry{ 
    Item item;
    int quanity;
    
    public ItemEntry(Item i, int quanity) {
        this.item = i;
        this.quanity = quanity;
    }

    public Item getItem() {
        return item;
    }

    public void setItem(Item i) {
        this.item = i;
    }

    public int getQuanity() {
        return quanity;
    }

    public void setQuanity(int quanity) {
        this.quanity = quanity;
    }
    


} 